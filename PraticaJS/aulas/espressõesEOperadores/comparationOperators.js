// Operadores de comparação

// Ira comparar valores e retornar um boolean como resposta à comparação

let one = 1
let two = 2

// Igual a: '=='
console.log(two == 1)
console.log(one == '1')

// Estitamente igual a: '==='
console.log(one === '1')
console.log(one === 1)

// Diferente de: '!='
console.log(one != two)
console.log(one != 1)
console.log(one != "1")

// Estritamente diferente de: '!=='
console.log(one !== two)
console.log(one !== 1)
console.log(one !== "1")

// Maior que: '>'
console.log(one > two)
console.log(two > one)
console.log(one > one)

// Maior ou igual a: >=
console.log(one >= two)
console.log(two >= one)
console.log(one >= one)

// Menor que: '<'
console.log(one < two)
console.log(two < one)
console.log(one < one)

// Menor ou igual a: <=
console.log(one <= two)
console.log(two <= one)
console.log(one <= one)